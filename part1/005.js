// Data types

/* String */

let userName = "Mahfooz";
console.log(userName);
console.log(typeof userName);


/* Number */

let age = 99;
console.log(age);
console.log(typeof age);

/*Boolean */

let isProduct = false;
console.log(isProduct);
console.log(typeof isProduct);

/* undefind */

let user;
console.log(user);
console.log(typeof user);

/* null */

let currency = null;  // no value

console.log(currency);
console.log(typeof currency);


/* object */

const person = {
    age : 23,
    userName : "Mahfooz",
    address : "Ghaziabad"
}

